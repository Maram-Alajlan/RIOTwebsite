<p class="paragraph_large">One of the initiatives of the National Transformation Program is coming up with a Low Cost
Communication Network for the Internet of Things (IoT) applications. IoT applications span several
areas of the Saudi Vision, Health, Energy Consumption, Security Monitoring, etc. These applications
can be also used in the concept of smart housing, smart campus, and smart workplace.

Establishing a professional research environment in the Robotics and Internet of Things (IoT) will
contribute to 2030 initiative. It will pursue and attract external research funds and grants from
industry and external research programs by providing consultation service as well as developing IoT
solutions and providing training services for Saudi Market.  </p>

<h2 class="section-heading">VISION </h2>
<p class="paragraph_large"> To be an internationally recognized centre in robotics and internet of things  </p>

<h2 class="section-heading">MISSION</h2>

<ul class="paragraph_large">
<li> To promote applied research on Robotics and Internet of Things in Prince Sultan University
through international collaboration with industrial and academic partners </li>
<li>To develop robotics and IoT solutions for real applications to sustain the economic and social
development in Saudi Arabia inline with 2030 vision. </li>

</ul>



<h2 class="section-heading">Objectives</h2>

<ul class="paragraph_large">
<li> Establish a professional research environment in PSU in the robotics and IoT research. </li>
<li> Attract external funds and grants to PSU.  </li>
<li> Provide a consultation service to the industry both locally and internationally.  </li>
<li> Develop robotics and IoT solution’s for the Saudi Market in collaboration with
international companies. </li>
<li> Providing training services to communities in the field of robotics and IoT.  </li>
<li> Contribute to the involvement of undergraduate and graduate students in professional
research and promote their intellectual and learning skills. </li>
<li> Establish international collaboration with reputed research groups and institutions
in robotics and IoT.  </li>
<li> Contribute to the enrichment of computer science and engineering programs in the
field of robotics and IoT  </li>

</ul>

<h2 class="section-heading">Benefits for PSU</h2>

<ul>
<li> Promoting the research impact of PSU around the world (improving its ranking among
                                                            world’s Universities).  </li>
<li> PSU will contribute to advancing the knowledge in the area of robotics and Internet
of Things.</li>
<li> Promote the research infrastructure and resources at the University through the
establishment of high-tech lab’s for robotics and Internet of Things.  </li>
<li> The research group will work towards getting external funds for research from industry
or other funding institutions such as KACST.  </li>
<li> Establishing a professional and effective collaboration with top universities in the
areas of robotics and IoT </li>

</ul>
